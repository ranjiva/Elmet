<div id="bay-window-left"><span class="HD3">How to Measure <strong>Curtains for Your Bay Windows</strong>?</span><br />
  <br />
  <span class="HD4">Suitable for curtain heading types:</span><br />
  Pencil Pleat and Eyelet.<br />
  <br />
To be sure of the best possible fit, take time to measure your window carefully. The curtain pole should be positioned 15cm above the window and extend 15cm - 20cm either side.
  <br />
  <br />
  <span class="HD5">A. Measuring the width:</span> Measure the total width of the rail (excluding finials). Don't forget to account for overlap - this  is an additional measurement, normally 2.5cm, that allows the hanging curtains to overlap in the middle when they are  closed. <br />
  <br />
  <span class="HD5">B. Measuring the drop:</span> Before measuring the drop, decide whether you want your curtains to fit below the sill or to the  floor. We suggest that: Curtains that fall below the sill finish 15cm below.<br />
    Floor length curtains finish 1.5cm from the floor.
    <br />
    <br />
    <strong>For pencil pleat curtains</strong> measure the drop from the eye - the small metal ring at the bottom of each curtain ring. This will prevent the pole or rings from being obscured by the curtains. <br />
    <br />
    <strong>For eyelet</strong> measure the drop from the top of the pole. <br />

