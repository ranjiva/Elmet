<h1>Measuring Your Curtains</h1>

<h2>Before You Start</h2>

<ul>
    <li>The sizes displayed on our website and in all communication are for a <strong>single</strong> curtain</li>
    <li>The sizes displayed on our website and in all communication are always communicated in the form width x drop</li>
    <li>The curtain prices displayed on our website are for a <strong>pair</strong> of curtains</li>
    <li>The drop size displayed on our website is the distance from the very top of the curtain to the very bottom of the curtain.</li>
    <li>Our curtains are packed completely flat. They will need to be gathered before being hung.</li>
    <li>Our curtains come in standard widths. However we can make curtains with widths greater than 152”</li>
    <li>Our curtains come in standard drops. However we provide a free drop alteration service. To take advantage of this order curtains in the next largest standard drop size and we will shorten for free.</li>
    <li>A curtain finished with 3” tape will need to hung using curtain hooks attached to either gliders on a track or rings on a curtain pole</li>
    <li>For accuracy, we recommend that curtain dimensions should be measured using a metal tape</li>
    <li>The top most edge of our 3” tape is attached to the top most edge of the curtains</li>
    
</ul>

<h2>Measuring Curtain Width</h2>

<ul>
    <li>Curtain widths should be measured using the length of the track/pole that the curtains will be mounted on.</li>
    <li>We recommend that the total width of the pair of curtains should be 1.5 – 2 times the length of the track/pole that the curtains are mounted on. This is to ensure that a sufficient number of pleats form when the curtains are gathered. The greater is the total width of the pair of curtains, the more the number and depth of pleats that form in the curtains when gathered and so the more full the curtains look.</li>
    <li>Finials or decorative ends, should not be included when measuring the length of the pole</li>
</ul>
