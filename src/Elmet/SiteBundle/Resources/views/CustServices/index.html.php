<h2>Returns Policy</h2>

<h3>Ready made products</h3>

<p>All our curtains and beddings are despatched in perfect condition. Therefore we respectfully ask  all customers to unpack our products carefully.</p>

<p>If in the unlikely event they are found not to be suitable please email <a href="mailto:returns@elmetcurtains.co.uk">returns@elmetcurtains.co.uk</a> within 7 days of receipt of the item(s), to obtain a returns authorisation number. Please include your name, address, PayPal receipt no., the reason for return, and advise note no. Upon receipt of above details we will then issue you with a returns authorisation number and arrange a convenient time and place, with yourself, for when our courier can pick up the items to be returned.</p>

<p>We kindly ask you to re-pack the items with the greatest care within their original packaging or similar and include the Returns number with the item(s). If we cannot supply your new requirements then we will  willingly  refund the purchase price providing that the curtains and beddings are returned in an unused re-saleable condition. We regret that we can not accept any returns with out a Returns authorisation number, nor be responsible for any item(s) sent directly to our premises.</p>

<p>To avoid disappointment caused by returning curtains where the design and colour are unsuitable we recommend that you send us a S.A.E. for a free swatch of your selected design and colour before you place your order.</p>

<h3>Made to measure products</h3>

<p>We provide free drop alteration service. We manufacture curtains and beddings to customer.s individual sizes .But regretfully, once they are made we cannot accept cancellation and therefore, unable to offer full refund.</p>

<p>In certain circumstances, we may consider offering a refund to the cost of next smaller stock size of curtain of that particular curtain range.</p>

